type GrantAdminRoleRequest struct {
	ActorID      uuid.UUID
	TargetUserID uuid.UUID
}

//nolint:dupl // Explicit separate use-case flow for readability and bounded-context clarity.
func (s *Service) GrantAdminRole(ctx context.Context, req *GrantAdminRoleRequest) error {
	if req == nil {
		return errs.ErrInvalidRequest
	}

	txCtx, tx, err := s.txFactory.Begin(ctx)
	if err != nil {
		return fmt.Errorf("failed to begin transaction: %w", err)
	}

	err = persistence.WithinTransaction(txCtx, tx, func() error {
		// Load actor and check permissions first.
		actor, err := s.repo.GetByID(txCtx, req.ActorID)
		if err != nil {
			return fmt.Errorf("failed to get actor: %w", err)
		}
		if err := actor.CanManageAdminRole(); err != nil {
			return err
		}

		// Load target and apply use-case.
		user, err := s.repo.GetByID(txCtx, req.TargetUserID)
		if err != nil {
			return fmt.Errorf("failed to get user: %w", err)
		}
		if err := user.GrantAdminRole(actor); err != nil {
			return err
		}

		if err := s.repo.Save(txCtx, user); err != nil {
			return fmt.Errorf("failed to save user: %w", err)
		}

		if err := s.eventDispatcher.Dispatch(txCtx, tx, user); err != nil {
			return fmt.Errorf("failed to dispatch domain events: %w", err)
		}

		return nil
	})
	if err != nil {
		return err
	}

	return nil
}

